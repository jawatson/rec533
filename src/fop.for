c###fop.for
      FUNCTION FOP(MONTH,ZENANG,PWRDB)
C.....THIS FUNCTION ASSIGNS THE RATIO OF THE OPERATIONAL MUF TO THE
C.....STANDARD MUF-FROM IWP 6/12 DECISION 36
      DIMENSION ISEAS(12), FACTOR(3,2,2)
      DATA ISEAS / 3,3,2,2,1,1,1,1,2,2,3,3 /
      DATA FACTOR/1.2, 1.25, 1.30, 1.1, 1.15, 1.20, 1.25, 1.30, 1.35,
     A 1.15, 1.20, 1.25/
C.....DETERMINE SEASONAL INDEX (FROM MONTH)
C.....INDEX=1 FOR SUMMER,=2 FOR EQUIOX, =3 FOR WINTER
      INDEX=ISEAS(MONTH)
C.....DETERIMINE DAY/NIGHT INDEX (FROM ZENITH ANGLE)
C.....JNDEX=1 FOR NIGHT,=2 FOR DAY
      JNDEX=1
      IF(ZENANG-102.) 100, 100, 110
  100 JNDEX=2
C.....DETERMINE POWER INDEX (FROM PWRDB)
C.....KNDEX=1 FOR .LE. 30 DBW,=2 FOR .GT. 30 DBW
  110 KNDEX=1
      IF(PWRDB-30.) 130, 130, 120
  120 KNDEX=2
  130 FOP=FACTOR(INDEX,JNDEX,KNDEX)
      RETURN
      END
