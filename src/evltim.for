      SUBROUTINE EVLTIM(KTRM,TSUM,GMT,GSUM)
C .....VERSION 21.JAN.86
      DIMENSION KTRM(*),TSUM(*),FSR(17)
      DATA INIL,FSR(1),OTIM/1,1.0,-1.0/,D2R/1.745329E-2/
      NHRM=2*KTRM(10)+1
      IF(INIL .EQ.0.AND.GMT.EQ.OTIM) GO TO 540
      TM1=(15.0*GMT-180.0)*D2R
      FSR(2)=SIN(TM1)
      FSR(3)=COS(TM1)
      DO 520 J=4,17,2
      FSR(J)=FSR(3)*FSR(J-2)+FSR(J-1)*FSR(2)
  520 FSR(J+1)=FSR(3)*FSR(J-1)-FSR(2)*FSR(J-2)
      INIL=0
      OTIM=GMT
  540 GSUM=TSUM(1)
      DO 560 J=2,NHRM
  560 GSUM=GSUM+TSUM(J)*FSR(J)
      RETURN
      END
