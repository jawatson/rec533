      SUBROUTINE SYSSY(G,Z,NN,FM)
C     SYSSY IS A TABLE LOOK-UP FOR DECILE VALUES OF THE DISTRIBUTION OF
C     EXCESS SYSTEM LOSS AND THE LIKELY PREDICTION OF ERRORS OF THE
C     SYSTEM LOSS IS DETERMINED.
C     G --- GEOMAGNETIC LATITUDE OF THE REFLECTION AREA
C     Z --- REFLECTION AREA LOCAL TIME
C     NN --- REFLECTION AREA
C     FM --- EXCESS SYSTEM LOSS
      COMMON/ONE/IFA(10),IMA(10),F2COF(13,76),FM3COF(9,49),SYS(9,16,6)
      COMMON /TWO /DUD(5,12,5),FAM(14,12),FAKP(29,16,6),FAKABP(2,6)
      COMMON / FILES / LUI,LUO,LU2,LU5,LU6,LU8,LU16,LU61,LU7,LU15
C     CHECK IF DATA FILE READ
      LJ=1
       J=Z+.5
      GG=ABS (G) * 5.729577E1
c          42.5 changed from 40.0  3/26/93
      KJ=((GG-42.5) / 5.)+2.0
      IF(KJ)100, 100, 105
 100  KJ=1
      GO TO 115
 105  IF(KJ-9)115, 115, 110
 110  KJ=9
      GO TO 115
 115  IF(G)125, 120, 120
 120  KK=0
      ND=0
      GO TO 130
 125  KK=8
      ND=3
 130  LJ=FLOAT (J) / 3.+.67
      IF(LJ-1)135, 140, 140
 135  LJ=8
 140  LOL=LJ+KK
      LD=FLOAT(J) / 6.+.67
      IF(LD-1)145, 150, 150
 145  LD=4
 150  FM=SYS (KJ, LOL, NN)
ccc      write(luo,'(''syssy='',3i5,f8.3)') kj,lol,nn,fm
      RETURN
      END
